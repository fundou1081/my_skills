def lookup(improve: int, worsen: int) -> list:
    key = (improve, worsen)
    if key in MATRIX:
        return MATRIX[key]
    key_rev = (worsen, improve)
    if key_rev in MATRIX:
        return MATRIX[key_rev]
    return []


def main():
    if len(sys.argv) >= 3:
        improve = int(sys.argv[1])
        worsen = int(sys.argv[2])
    else:
        print("用法: python3 matrix_lookup.py <改善参数编号> <恶化参数编号>")
        print("例如: python3 matrix_lookup.py 9 27")
        print("\n参数编号 1-39，见 references/39_parameters.md")
        return

    if not (1 <= improve <= 39 and 1 <= worsen <= 39):
        print("参数编号必须在 1-39 之间")
        return

    principles = lookup(improve, worsen)

    print(f"\n改善参数 #{improve}: {PARAMS.get(improve, '未知')}")
    print(f"恶化参数 #{worsen}: {PARAMS.get(worsen, '未知')}")
    print()
    if principles:
        print("推荐的创新原理（按优先级）:")
        for rank, pid in enumerate(principles, 1):
            print(f"  {rank}. #{pid:>2} {PRINCIPLES.get(pid, '未知')}")
    else:
        print("未找到对应条目，建议手动选择以下常用原理: 1, 2, 3, 13, 15, 17, 22, 35")
        principles = [1, 2, 3, 13, 15, 17, 22, 35]

    result = {
        "improve_param_no": improve,
        "improve_param_name": PARAMS.get(improve),
        "worsen_param_no": worsen,
        "worsen_param_name": PARAMS.get(worsen),
        "recommended_principles": [
            {"rank": i+1, "id": pid, "name": PRINCIPLES.get(pid)}
            for i, pid in enumerate(principles)
        ],
    }
    print(f"\n[SCRIPT_OUTPUT_JSON]{json.dumps(result, ensure_ascii=False)}[/SCRIPT_OUTPUT_JSON]")


if __name__ == "__main__":
    main()
